// ==================== UTILITY FUNCTIONS ====================

// Get all exercises for a specific level
export function getGrammarExercisesByLevel(level: string): GrammarExercise[] {
  switch (level.toUpperCase()) {
    case 'A1': return a1GrammarExercises;
    case 'A2': return a2GrammarExercises;
    case 'B1': return b1GrammarExercises;
    case 'B2': return b2GrammarExercises;
    case 'C1': return c1GrammarExercises;
    case 'C2': return c2GrammarExercises;
    default: return [];
  }
}

// Get exercises by category for a specific level
export function getGrammarExercisesByCategory(level: string, category: string): GrammarExercise[] {
  const levelExercises = getGrammarExercisesByLevel(level);
  return levelExercises.filter(ex => ex.category.toLowerCase().includes(category.toLowerCase()));
}

// Get random exercises for a level
export function getRandomGrammarExercises(level: string, count: number = 10): GrammarExercise[] {
  const exercises = getGrammarExercisesByLevel(level);
  const shuffled = [...exercises].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, exercises.length));
}

